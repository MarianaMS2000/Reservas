    private void aceptar_butActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptar_butActionPerformed
        String id_pas = tf_id_pas.getText().trim();
        String nom_pas = tf_nom_pas.getText().trim();
        String silla = tf_silla_pas.getText().trim();
        String tipoTexto = cb_tipo.getSelectedItem().toString();
        if (id_pas.isEmpty() || nom_pas.isEmpty() || tipoTexto.equals("Seleccionar..")
                || (tipoTexto.equals("Ejecutiva") && silla.isEmpty())) {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos.");
            return;
        }
        char tipo = tipoTexto.toUpperCase().charAt(0);
        if (Principal.registrarReserva(id_pas, nom_pas, tipo, silla)) {
            Principal.etr++;
            Visor();
            Limpiar_campos();
        }

    }//GEN-LAST:event_aceptar_butActionPerformed

