    public void Limpiar_campos() {
        tf_id_pas.requestFocus();
        tf_nom_pas.setText("");
        cb_tipo.setSelectedIndex(0);
        tf_silla_pas.setText("");

    }

    public static void Visor() {
        tbmod.setRowCount(0);

        for (int i = 0; i < Principal.etr; i++) {
            if (Principal.vres[i] != null) {

                String tipo = Principal.vres[i].getTipo() == 'B' ? "Basic"
                        : Principal.vres[i].getTipo() == 'M' ? "Medium" : "Ejecutiva";

                tbmod.addRow(new Object[]{
                    Principal.vres[i].getNum_res(),
                    Principal.vres[i].getId_pas(),
                    Principal.vres[i].getNom_pas(),
                    tipo,
                    Principal.vres[i].getSilla(),
                    Principal.vres[i].getMorral(),
                    Principal.vres[i].getMaleta(),
                    Principal.vres[i].getCat()
                });
            }
        }
    }

